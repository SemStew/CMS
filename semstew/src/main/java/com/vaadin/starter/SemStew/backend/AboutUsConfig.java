package com.vaadin.starter.SemStew.backend;


public class AboutUsConfig {

  private long idLanguage;
  private String header;
  private String description;
  private String fotogalleryHeader;


  public long getIdLanguage() {
    return idLanguage;
  }

  public void setIdLanguage(long idLanguage) {
    this.idLanguage = idLanguage;
  }


  public String getHeader() {
    return header;
  }

  public void setHeader(String header) {
    this.header = header;
  }


  public String getDescription() {
    return description;
  }

  public void setDescription(String description) {
    this.description = description;
  }


  public String getFotogalleryHeader() {
    return fotogalleryHeader;
  }

  public void setFotogalleryHeader(String fotogalleryHeader) {
    this.fotogalleryHeader = fotogalleryHeader;
  }

}
